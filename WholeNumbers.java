public class WholeNumbers {
    public static void main(String[] args) {
        int town=10000;
        int city=100000;
        int state=90000000;
        int country=1000000000;
        long globalPopulation=100000000000000L;
        long googleSearchers=5000000000000L;
        System.out.println("The population in a town "+ town);
        System.out.println("The population in a city "+ city);
        System.out.println("The population in a state "+ state);
        System.out.println("The population in a country "+ country);
        System.out.println("The population in the world "+globalPopulation);
        System.out.println("The googleusers in a day "+ googleSearchers);
    }
    
}
